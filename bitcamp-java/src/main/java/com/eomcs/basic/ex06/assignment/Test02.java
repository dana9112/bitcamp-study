package com.eomcs.basic.ex06.assignment;

import java.util.Scanner;

public class Test02 {

  public static void main(String[] args) {

    
    Scanner keyScan = new Scanner(System.in);
    System.out.print("가로 길이? ");
    int len = keyScan.nextInt();
    

    int i = 1;
    while (i <= len) {
      int j = 1;
      while (j <= i) {
        System.out.print("*");
        j++;
      }
      System.out.println("");
      i++;
    }
    
    i = len - 1;
    while (i >= 1) {
      int j = 1;
      while (j <= i) {
        System.out.print("*");
        j++;
      }
      System.out.println("");
      i--;
    }
  }
}
